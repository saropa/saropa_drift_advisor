/**
 * nls-coverage — Manifest NLS value-coverage measure (plan 75 §2/§6, System A).
 *
 * verify-nls proves keys EXIST in every bundle; it says nothing about whether the
 * values were actually TRANSLATED. This script measures that gap: per locale, how
 * many values DIFFER from the English base (= translated) versus equal it
 * (= still English). It regenerates the committed snapshot
 * `src/l10n/nls-coverage-data.ts` and prints a coverage table.
 *
 * Two modes:
 *   (default)  generate — rewrite nls-coverage-data.ts and print the table.
 *              Run after editing any package.nls*.json: `npm run generate:nls-coverage`.
 *   --check    verify — regenerate in memory and FAIL only if the committed file is
 *              STALE (out of date vs the bundles). It does NOT fail on low coverage —
 *              coverage is reported, never gated. Wired into `compile` / CI.
 *
 * WHAT THIS CATCHES vs WHAT IT DOES NOT: it catches "value still equals English".
 * It cannot tell a real translation from a legitimate cross-locale cognate that is
 * genuinely identical to English (e.g. "Error" in several languages) — such a value
 * reads as untranslated here. So a sub-100% figure is a FLOOR on translation work
 * remaining, not a precise defect count. Never report this percent as a quality
 * guarantee.
 */
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const extDir = join(dirname(fileURLToPath(import.meta.url)), '..');
const dataFile = join(extDir, 'src', 'l10n', 'nls-coverage-data.ts');
const checkMode = process.argv.includes('--check');

const base = JSON.parse(readFileSync(join(extDir, 'package.nls.json'), 'utf8'));
const baseKeys = Object.keys(base);
const totalKeys = baseKeys.length;

// Discover per-locale bundles: package.nls.<locale>.json (NOT the base package.nls.json).
const localeFiles = readdirSync(extDir)
  .filter((f) => /^package\.nls\.[\w-]+\.json$/.test(f))
  .sort();

/** locale → { translated, total, pct } */
const detail = {};
for (const file of localeFiles) {
  const locale = file.replace(/^package\.nls\.([\w-]+)\.json$/, '$1');
  const bundle = JSON.parse(readFileSync(join(extDir, file), 'utf8'));
  let translated = 0;
  for (const key of baseKeys) {
    const v = bundle[key];
    // A key absent from the locale is "missing" (verify-nls catches that) and is
    // NOT counted as translated; a present value equal to English is untranslated.
    if (typeof v === 'string' && v !== base[key]) {
      translated++;
    }
  }
  const pct = totalKeys === 0 ? 100 : Math.round((translated / totalKeys) * 100);
  detail[locale] = { translated, total: totalKeys, pct };
}

const locales = Object.keys(detail).sort();

// Build the generated TS module. Deterministic (sorted keys, NO timestamp) so the
// --check staleness comparison is stable — a date here would always read as stale.
function buildDataFile() {
  const lines = [];
  lines.push('/**');
  lines.push(' * AUTO-GENERATED by extension/scripts/nls-coverage.mjs — DO NOT EDIT BY HAND.');
  lines.push(' * Regenerate with: npm run generate:nls-coverage');
  lines.push(' *');
  lines.push(' * Manifest NLS value-coverage snapshot (plan 75 §2). `NLS_COVERAGE` maps each');
  lines.push(' * translated locale to the percent of the ' + totalKeys + ' manifest keys whose value');
  lines.push(' * differs from English. English is the source (always 100%) and is not listed.');
  lines.push(' * Consumed by the future activation coverage notice; guarded for staleness by');
  lines.push(' * `npm run verify:nls-coverage`.');
  lines.push(' */');
  lines.push('');
  lines.push(`export const NLS_TOTAL_KEYS = ${totalKeys};`);
  lines.push('');
  lines.push('/** Translated-locale → percent of manifest values that differ from English (0–100). */');
  if (locales.length === 0) {
    lines.push('export const NLS_COVERAGE: Record<string, number> = {};');
  } else {
    lines.push('export const NLS_COVERAGE: Record<string, number> = {');
    for (const loc of locales) {
      lines.push(`  '${loc}': ${detail[loc].pct},`);
    }
    lines.push('};');
  }
  lines.push('');
  return lines.join('\n');
}

const generated = buildDataFile();
const normalize = (s) => s.replace(/\r\n/g, '\n');

function printTable() {
  if (locales.length === 0) {
    console.log(`nls-coverage: ${totalKeys} base keys; no translated locale bundles found (English-only).`);
    console.log('  Measures values differing from English; a legitimate cognate identical to English reads as untranslated.');
    return;
  }
  console.log(`nls-coverage: ${totalKeys} manifest keys; ${locales.length} translated locale(s):`);
  for (const loc of locales) {
    const d = detail[loc];
    console.log(`  ${loc.padEnd(7)} ${String(d.translated).padStart(4)}/${d.total}  ${String(d.pct).padStart(3)}%`);
  }
  console.log('  (Counts values differing from English; identical legitimate cognates read as untranslated — floor, not a quality guarantee.)');
}

if (checkMode) {
  let current = null;
  try {
    current = readFileSync(dataFile, 'utf8');
  } catch {
    console.error(`verify:nls-coverage: FAIL — ${dataFile} is missing. Run: npm run generate:nls-coverage`);
    process.exit(1);
  }
  if (normalize(current) !== normalize(generated)) {
    console.error('verify:nls-coverage: FAIL — nls-coverage-data.ts is STALE vs package.nls*.json.');
    console.error('  Run: npm run generate:nls-coverage  (then commit the regenerated file)');
    process.exit(1);
  }
  console.log('verify:nls-coverage: OK — nls-coverage-data.ts is current.');
  printTable();
} else {
  writeFileSync(dataFile, generated, 'utf8');
  console.log(`nls-coverage: wrote ${dataFile}`);
  printTable();
}
